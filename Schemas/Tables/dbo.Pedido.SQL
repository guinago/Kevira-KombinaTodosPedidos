CREATE TABLE Pedido (
    PedidoID INT NOT NULL IDENTITY(1, 1),	
    ClienteID INT NOT NULL,
    CompradorID INT NOT NULL,
    TransportadoraID INT NOT NULL,
    CondicaoPagamentoID INT NOT NULL,
    Telefone VARCHAR(MAX) NOT NULL,
    Email VARCHAR(20) NOT NULL,
    CEP VARCHAR(MAX) NOT NULL,
    EnderecoCEP VARCHAR(MAX) NOT NULL,
    EnderecoCompleto VARCHAR(MAX) NOT NULL,
    Estado VARCHAR(MAX) NOT NULL,
    Municipio VARCHAR(MAX) NOT NULL,
    EnderecoBairro VARCHAR(MAX) NOT NULL,
    DataEntregaInicio DATETIME NOT NULL,
    DataEntregaFim DATETIME NOT NULL,
    Observacao VARCHAR(MAX) NULL,
    Restricao VARCHAR(MAX) NULL,
    NotaFiscal VARCHAR(MAX) NULL,
    Status INT NOT NULL DEFAULT 0,
	Datacriacao DATETIME NOT NULL,
    DataModif DATETIME,
    PRIMARY KEY (PedidoID),
    FOREIGN KEY (CompradorID) REFERENCES Usuarios(UsuarioID),
    FOREIGN KEY (ClienteID) REFERENCES Cliente(ClienteID),
	FOREIGN KEY (TransportadoraID) REFERENCES Transportadora(TransportadoraID),
    FOREIGN KEY (CondicaoPagamentoID) REFERENCES CondicaoPagamento(CondicaoPagamentoID)
);